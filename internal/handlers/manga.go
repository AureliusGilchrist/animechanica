// HandleGetLocalMangaPage
//
//	@summary returns a local manga page.
//	@route /api/v1/manga/local-page/{path} [GET]
//	@returns manga.PageContainer
func (h *Handler) HandleGetLocalMangaPage(c echo.Context) error {

	path := c.Param("path")
	h.App.Logger.Debug().Str("rawPath", path).Msg("manga: HandleGetLocalMangaPage - raw path")
	path, err := url.PathUnescape(path)
	if err != nil {
		return h.RespondWithError(c, err)
	}
	h.App.Logger.Debug().Str("unescapedPath", path).Msg("manga: HandleGetLocalMangaPage - unescaped path")

	path = strings.TrimPrefix(path, manga_providers.LocalServePath)
	h.App.Logger.Debug().Str("finalPath", path).Str("localServePath", manga_providers.LocalServePath).Msg("manga: HandleGetLocalMangaPage - final path after trimming")

	providerExtension, ok := extension.GetExtension[extension.MangaProviderExtension](h.App.ExtensionRepository.GetExtensionBank(), manga_providers.LocalProvider)
	if !ok {
		return h.RespondWithError(c, errors.New("manga: Local provider not found"))
	}

	localProvider, ok := providerExtension.GetProvider().(*manga_providers.Local)
	if !ok {
		return h.RespondWithError(c, errors.New("manga: Local provider not found"))
	}

	reader, err := localProvider.ReadPage(path)
	if err != nil {
		return h.RespondWithError(c, err)
	}

	return c.Stream(http.StatusOK, "image/jpeg", reader)
}

// createSyntheticMangaEntry creates a manga entry for synthetic IDs using downloaded manga metadata
func (h *Handler) createSyntheticMangaEntry(syntheticID int) (*manga.Entry, error) {
	// Get the downloaded manga series metadata
	downloadedSeries, err := h.App.MangaDownloader.GetDownloadedMangaList()
	if err != nil {
		return nil, fmt.Errorf("failed to get downloaded manga series: %w", err)
	}

	// Find the series with the matching synthetic ID
	var targetSeries *manga.DownloadedMangaSeries
	for _, series := range downloadedSeries {
		if series.MediaID == syntheticID {
			targetSeries = &series
			break
		}
	}

	if targetSeries == nil {
		return nil, fmt.Errorf("no downloaded manga found for synthetic ID %d", syntheticID)
	}

	// Create comprehensive synthetic manga entry using the downloaded metadata
	status := anilist.MediaStatusFinished
	format := anilist.MediaFormatManga
	mediaType := anilist.MediaTypeManga
	description := fmt.Sprintf("Downloaded manga series with %d chapters. This manga was downloaded using the En Masse Downloader and contains local content ready for reading.", targetSeries.ChapterCount)
	chapterCount := targetSeries.ChapterCount
	volumes := (chapterCount + 9) / 10 // Estimate volumes (roughly 10 chapters per volume)
	meanScore := 75                    // Default reasonable score
	countryOfOrigin := "JP"            // Default to Japan for manga

	// Create synonyms from series title variations
	synonymStrings := []string{targetSeries.SeriesTitle}
	// Convert to []*string for AniList compatibility
	synonyms := make([]*string, len(synonymStrings))
	for i, synonym := range synonymStrings {
		synonyms[i] = &synonym
	}

	// Create start and end dates (estimate based on chapter count and current time)
	now := time.Now()
	estimatedDurationMonths := targetSeries.ChapterCount / 4 // Assume ~4 chapters per month
	if estimatedDurationMonths < 6 {
		estimatedDurationMonths = 6 // Minimum 6 months
	}
	startDate := now.AddDate(0, -estimatedDurationMonths, 0)
	endDate := now.AddDate(0, -1, 0) // Ended 1 month ago

	entry := &manga.Entry{
		MediaId: syntheticID,
		Media: &anilist.BaseManga{
			ID:              syntheticID,
			Type:            &mediaType,
			Format:          &format,
			Status:          &status,
			Chapters:        &chapterCount,
			Volumes:         &volumes,
			MeanScore:       &meanScore,
			CountryOfOrigin: &countryOfOrigin,
			Synonyms:        synonyms,
			Description:     &description,
			Title: &anilist.BaseManga_Title{
				Romaji:        &targetSeries.SeriesTitle,
				English:       &targetSeries.SeriesTitle,
				Native:        &targetSeries.SeriesTitle,
				UserPreferred: &targetSeries.SeriesTitle,
			},
			CoverImage: &anilist.BaseManga_CoverImage{
				Large: func() *string {
					if targetSeries.CoverImagePath != "" {
						// Format cover image path as API endpoint URL
						coverURL := fmt.Sprintf("/api/v1/manga/local-page/%s", url.QueryEscape(targetSeries.CoverImagePath))
						return &coverURL
					}
					return nil
				}(),
				ExtraLarge: func() *string {
					if targetSeries.CoverImagePath != "" {
						coverURL := fmt.Sprintf("/api/v1/manga/local-page/%s", url.QueryEscape(targetSeries.CoverImagePath))
						return &coverURL
					}
					return nil
				}(),
				Medium: func() *string {
					if targetSeries.CoverImagePath != "" {
						coverURL := fmt.Sprintf("/api/v1/manga/local-page/%s", url.QueryEscape(targetSeries.CoverImagePath))
						return &coverURL
					}
					return nil
				}(),
			},
			StartDate: &anilist.BaseManga_StartDate{
				Year:  &[]int{startDate.Year()}[0],
				Month: &[]int{int(startDate.Month())}[0],
				Day:   &[]int{startDate.Day()}[0],
			},
			EndDate: &anilist.BaseManga_EndDate{
				Year:  &[]int{endDate.Year()}[0],
				Month: &[]int{int(endDate.Month())}[0],
				Day:   &[]int{endDate.Day()}[0],
			},
		},
		// No EntryListData since this isn't in the user's AniList
		EntryListData: nil,
	}

	return entry, nil
}

