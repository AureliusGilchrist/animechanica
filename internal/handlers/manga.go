// createSyntheticChapterContainer creates a chapter container for synthetic IDs using downloaded manga metadata
func (h *Handler) createSyntheticChapterContainer(syntheticID int, provider string) (*manga.ChapterContainer, error) {
	// Get the downloaded manga series metadata
	downloadedSeries, err := h.App.MangaDownloader.GetDownloadedMangaList()
	if err != nil {
		return nil, fmt.Errorf("failed to get downloaded manga series: %w", err)
	}

	// Find the series with the matching synthetic ID
	var targetSeries *manga.DownloadedMangaSeries
	for _, series := range downloadedSeries {
		if series.MediaID == syntheticID {
			targetSeries = &series
			break
		}
	}

	if targetSeries == nil {
		return nil, fmt.Errorf("no downloaded manga found for synthetic ID %d", syntheticID)
	}

	// Sort chapters numerically instead of alphabetically
	sort.Slice(targetSeries.Chapters, func(i, j int) bool {
		// Extract numeric part from chapter numbers for proper sorting
		chapterI, errI := strconv.ParseFloat(targetSeries.Chapters[i].ChapterNumber, 64)
		chapterJ, errJ := strconv.ParseFloat(targetSeries.Chapters[j].ChapterNumber, 64)

		// If both are valid numbers, sort numerically
		if errI == nil && errJ == nil {
			return chapterI < chapterJ
		}

		// Fallback to string comparison if parsing fails
		return targetSeries.Chapters[i].ChapterNumber < targetSeries.Chapters[j].ChapterNumber
	})

	// Create chapter container from downloaded chapters
	chapters := make([]*hibikemanga.ChapterDetails, 0, len(targetSeries.Chapters))
	for _, chapter := range targetSeries.Chapters {
		// Create proper chapter ID for local provider
		chapterID := fmt.Sprintf("%d_%s", syntheticID, chapter.ChapterNumber)

		chapterDetails := &hibikemanga.ChapterDetails{
			ID:       chapterID,
			Title:    chapter.ChapterTitle,
			Chapter:  chapter.ChapterNumber,
			Provider: "local",             // Force local provider for downloaded chapters
			URL:      chapter.ChapterPath, // Use chapter path as URL for local provider
			Index:    0,                   // Will be set by container
		}
		chapters = append(chapters, chapterDetails)
	}

	// Create the chapter container
	container := &manga.ChapterContainer{
		Provider: "local", // Force local provider
		MediaId:  syntheticID,
		Chapters: chapters,
	}

	// Set chapter indices
	for i, chapter := range container.Chapters {
		chapter.Index = uint(i)
	}

	return container, nil
}
